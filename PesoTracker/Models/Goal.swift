import Foundation

// MARK: - Goal Types
enum GoalType: String, CaseIterable, Codable {
    case main = "main"           // Long-term primary goal
    case shortTerm = "short"     // Auto-generated milestones
    case maintenance = "maintenance" // Weight maintenance
    
    var priority: Int {
        switch self {
        case .main: return 1
        case .shortTerm: return 2
        case .maintenance: return 3
        }
    }
    
    var displayName: String {
        switch self {
        case .main: return "Main Goal"
        case .shortTerm: return "Milestone"
        case .maintenance: return "Maintenance"
        }
    }
    
    var emoji: String {
        switch self {
        case .main: return "🏆"
        case .shortTerm: return "🎯"
        case .maintenance: return "⚖️"
        }
    }
}

struct Goal: Codable, Identifiable {
    let id: Int
    let userId: Int
    let targetWeight: Double
    let targetDate: String
    let type: GoalType
    let isAutoGenerated: Bool
    let parentGoalId: Int?       // Links to main goal for short-term goals
    let milestoneNumber: Int?    // Which milestone (1st, 2nd, etc.)
    let createdAt: String?
    let updatedAt: String?
    
    enum CodingKeys: String, CodingKey {
        case id
        case userId = "user_id"
        case targetWeight = "target_weight"
        case targetDate = "target_date"
        case type
        case isAutoGenerated = "is_auto_generated"
        case parentGoalId = "parent_goal_id"
        case milestoneNumber = "milestone_number"
        case createdAt = "created_at"
        case updatedAt = "updated_at"
    }
    
    // Custom decoder to handle target_weight as string from API
    init(from decoder: Decoder) throws {
        let container = try decoder.container(keyedBy: CodingKeys.self)
        
        id = try container.decode(Int.self, forKey: .id)
        userId = try container.decode(Int.self, forKey: .userId)
        targetDate = try container.decode(String.self, forKey: .targetDate)
        type = try container.decodeIfPresent(GoalType.self, forKey: .type) ?? .main
        isAutoGenerated = try container.decodeIfPresent(Bool.self, forKey: .isAutoGenerated) ?? false
        parentGoalId = try container.decodeIfPresent(Int.self, forKey: .parentGoalId)
        milestoneNumber = try container.decodeIfPresent(Int.self, forKey: .milestoneNumber)
        createdAt = try container.decodeIfPresent(String.self, forKey: .createdAt)
        updatedAt = try container.decodeIfPresent(String.self, forKey: .updatedAt)
        
        // Handle targetWeight as either String or Double
        if let weightString = try? container.decode(String.self, forKey: .targetWeight) {
            targetWeight = Double(weightString) ?? 0.0
        } else {
            targetWeight = try container.decode(Double.self, forKey: .targetWeight)
        }
    }
    
    // Custom encoder for sending data back to API
    func encode(to encoder: Encoder) throws {
        var container = encoder.container(keyedBy: CodingKeys.self)
        
        try container.encode(id, forKey: .id)
        try container.encode(userId, forKey: .userId)
        try container.encode(targetWeight, forKey: .targetWeight) // Send as Double
        try container.encode(targetDate, forKey: .targetDate)
        try container.encode(type, forKey: .type)
        try container.encode(isAutoGenerated, forKey: .isAutoGenerated)
        try container.encodeIfPresent(parentGoalId, forKey: .parentGoalId)
        try container.encodeIfPresent(milestoneNumber, forKey: .milestoneNumber)
        try container.encodeIfPresent(createdAt, forKey: .createdAt)
        try container.encodeIfPresent(updatedAt, forKey: .updatedAt)
    }
    
    // Computed properties
    var isMainGoal: Bool { type == .main }
    var isShortTermGoal: Bool { type == .shortTerm }
    var isMaintenanceGoal: Bool { type == .maintenance }
    
    var displayTitle: String {
        switch type {
        case .main:
            return "Main Goal: \(formattedTargetWeight)"
        case .shortTerm:
            if let milestone = milestoneNumber {
                return "Milestone #\(milestone): \(formattedTargetWeight)"
            } else {
                return "Milestone: \(formattedTargetWeight)"
            }
        case .maintenance:
            return "Maintain: \(formattedTargetWeight)"
        }
    }
    
    var formattedTargetWeight: String {
        return String(format: "%.1f kg", targetWeight)
    }
    
    var formattedTargetDate: String {
        let dateFormatter = DateFormatter()
        // Try different date formats that the API might return
        let formats = [
            "yyyy-MM-dd'T'HH:mm:ss.SSS'Z'",
            "yyyy-MM-dd'T'HH:mm:ss'Z'",
            "yyyy-MM-dd"
        ]
        
        for format in formats {
            dateFormatter.dateFormat = format
            if let parsedDate = dateFormatter.date(from: targetDate) {
                let displayFormatter = DateFormatter()
                displayFormatter.dateFormat = "MMM dd, yyyy"
                return displayFormatter.string(from: parsedDate)
            }
        }
        
        // If no format works, return the original string
        return targetDate
    }
}

