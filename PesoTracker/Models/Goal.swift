    var isMainGoal: Bool { type == .main }
    var displayTitle: String {
        switch type {
        case .main:
            return "🏆 Main Goal: \(formattedTargetWeight)"
        case .shortTerm:
            if let milestone = milestoneNumber {
                return "🎯 Milestone #\(milestone): \(formattedTargetWeight)"
            } else {
                return "🎯 Milestone: \(formattedTargetWeight)"
            }
        case .maintenance:
            return "⚖️ Maintain: \(formattedTargetWeight)"
        }
    }
    
    var formattedTargetWeight: String {
        return String(format: "%.1f kg", targetWeight)
    }
    
    var formattedTargetDate: String {
        let dateFormatter = DateFormatter()
        let formats = [
            "yyyy-MM-dd'T'HH:mm:ss.SSS'Z'",
            "yyyy-MM-dd'T'HH:mm:ss'Z'",
            "yyyy-MM-dd"
        ]
        
        for format in formats {
            dateFormatter.dateFormat = format
            if let parsedDate = dateFormatter.date(from: targetDate) {
                let displayFormatter = DateFormatter()
                displayFormatter.dateFormat = "MMM dd, yyyy"
                return displayFormatter.string(from: parsedDate)
            }
        }
        
        return targetDate
    }
    
    /// Convert from Goal to SmartGoal
    init(from goal: Goal, achievedAt: String? = nil) {
        self.id = goal.id
        self.type = goal.type
        self.targetWeight = goal.targetWeight
        self.targetDate = goal.targetDate
        self.isAutoGenerated = goal.isAutoGenerated
        self.parentGoalId = goal.parentGoalId
        self.milestoneNumber = goal.milestoneNumber
        self.createdAt = goal.createdAt
        self.achievedAt = achievedAt
    }
    
    /// Full initializer for creating new SmartGoals
    init(
        id: Int,
        type: GoalType,
        targetWeight: Double,
        targetDate: String,
        isAutoGenerated: Bool,
        parentGoalId: Int?,
        milestoneNumber: Int?,
        createdAt: String?,
        achievedAt: String?
    ) {
        self.id = id
        self.type = type
        self.targetWeight = targetWeight
        self.targetDate = targetDate
        self.isAutoGenerated = isAutoGenerated
        self.parentGoalId = parentGoalId
        self.milestoneNumber = milestoneNumber
        self.createdAt = createdAt
        self.achievedAt = achievedAt
    }
}

// MARK: - Goal Hierarchy Management

struct GoalHierarchy {
    let mainGoal: Goal?
    let shortTermGoals: [Goal]
    let maintenanceGoals: [Goal]
    
    var allGoals: [Goal] {
        var goals: [Goal] = []
        if let main = mainGoal { goals.append(main) }
        goals.append(contentsOf: shortTermGoals)
        goals.append(contentsOf: maintenanceGoals)
        return goals
    }
    
    var activeGoals: [Goal] {
        allGoals.filter { goal in
            // A goal is active if it's not achieved yet
            // We'll determine achievement status based on current weight vs target
            true // For now, we'll handle achievement logic elsewhere
        }
    }
    
    var currentMilestone: Goal? {
        // Return the next unachieved short-term goal
        shortTermGoals.first // Simplified for now
    }
    
    init(from goals: [Goal]) {
        self.mainGoal = goals.first { $0.type == .main }
        self.shortTermGoals = goals.filter { $0.type == .shortTerm }.sorted { 
            ($0.milestoneNumber ?? 0) < ($1.milestoneNumber ?? 0) 
        }
        self.maintenanceGoals = goals.filter { $0.type == .maintenance }
    }
}

// MARK: - Goal Utilities

extension Goal {
    /// Check if this goal should be considered achieved based on current weight
    func isAchieved(currentWeight: Double) -> Bool {
        // For weight loss goals, achieved when current weight <= target weight
        // For maintenance goals, achieved when within ±0.5kg of target
        switch type {
        case .main, .shortTerm:
            return currentWeight <= targetWeight
        case .maintenance:
            return abs(currentWeight - targetWeight) <= 0.5
        }
    }
    
    /// Calculate progress percentage towards this goal
    func progressPercentage(currentWeight: Double, startWeight: Double) -> Double {
        guard startWeight != targetWeight else { return 100.0 }
        
        let totalDistance = abs(startWeight - targetWeight)
        let currentDistance = abs(startWeight - currentWeight)
        let progress = (currentDistance / totalDistance) * 100.0
        
        return min(max(progress, 0.0), 100.0)
    }
    
    /// Get days remaining until target date
    var daysRemaining: Int? {
        let dateFormatter = DateFormatter()
        let formats = [
            "yyyy-MM-dd'T'HH:mm:ss.SSS'Z'",
            "yyyy-MM-dd'T'HH:mm:ss'Z'",
            "yyyy-MM-dd"
        ]
        
        for format in formats {
            dateFormatter.dateFormat = format
            if let targetDate = dateFormatter.date(from: self.targetDate) {
                let calendar = Calendar.current
                let days = calendar.dateComponents([.day], from: Date(), to: targetDate).day
                return days
            }
        }
        
        return nil
    }
    
    /// Check if goal is overdue
    var isOverdue: Bool {
        guard let days = daysRemaining else { return false }
        return days < 0
    }
}